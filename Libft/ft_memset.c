#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *cur;

	if (!len)
		return (b);
	cur = (unsigned char *)b;
	while (len--)
	{
		*cur = (unsigned char)c;
		if (len)
			cur++;
	}
	return (b);
}
